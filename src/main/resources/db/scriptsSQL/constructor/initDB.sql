CREATE SEQUENCE IF NOT EXISTS CONSTRUCTOR_SEQ
  AS INTEGER
    START WITH 1000;


CREATE TABLE IF NOT EXISTS answerkits
(
  id         INTEGER GENERATED BY DEFAULT AS SEQUENCE CONSTRUCTOR_SEQ  PRIMARY KEY,
  name       VARCHAR(255),
  answerType VARCHAR(255)
);


CREATE TABLE IF NOT EXISTS questionkits
(
  id   INTEGER GENERATED BY DEFAULT AS SEQUENCE CONSTRUCTOR_SEQ  PRIMARY KEY,
  name VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS answervars
(
  id           INTEGER GENERATED BY DEFAULT AS SEQUENCE CONSTRUCTOR_SEQ  PRIMARY KEY,
  name         VARCHAR(255),
  answerkit_id INTEGER,

  FOREIGN KEY (answerkit_id) REFERENCES answerkits (id)
    ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS questionvars
(
  id             INTEGER GENERATED BY DEFAULT AS SEQUENCE CONSTRUCTOR_SEQ  PRIMARY KEY,
  number        INTEGER,
  name           VARCHAR(255),
  superQuestionVarId INTEGER,
  answerkit_id   INTEGER NULL,
  questionkit_id   INTEGER,

  FOREIGN KEY (answerkit_id) REFERENCES answerkits (id)
    ON DELETE CASCADE,
  FOREIGN KEY (questionkit_id) REFERENCES questionkits (id)
    ON DELETE CASCADE
);




