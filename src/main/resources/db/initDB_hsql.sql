CREATE SEQUENCE GLOBAL_SEQ
  AS INTEGER








CREATE TABLE groups
(
  id      INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ  PRIMARY KEY,
  name  VARCHAR(255),
);




CREATE TABLE persons
(
    id      INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ  PRIMARY KEY,
  name  VARCHAR(255),
  group_id      INTEGER,
  FOREIGN KEY (group_id) REFERENCES ORDERS (id)
    ON DELETE CASCADE
);
CREATE UNIQUE INDEX persons_unique_group_idx ON groups (group_id);






